from pydantic import BaseModel, UUID4


class UnprocessedVideoUploaded(BaseModel):
    """
    Модель данных для события загрузки необработанного видео.
    
    Содержит информацию о загруженном видео, которое требует конвертации.
    Используется для передачи между сервисами через брокер сообщений.
    """
    user_id: int
    video_path: str


class ConfirmVideoHlsConverting(BaseModel):
    """
    Модель данных для подтверждения успешной конвертации видео в HLS.
    
    Содержит UUID видео, которое было успешно сконвертировано.
    Используется для обновления статуса видео в базе данных.
    """
    uuid: UUID4
